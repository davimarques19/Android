package com.example.iteris_cards_api;

public class Property {

    private String tv_address;
    private String tv_price;
    private String tv_owner;
    private String tv_type;
    private String image;

    public String getTv_address() {
        return tv_address;
    }

    public void setTv_address(String tv_address) {
        this.tv_address = tv_address;
    }

    public String getTv_price() {
        return tv_price;
    }

    public void setTv_price(String tv_price) {
        this.tv_price = tv_price;
    }

    public String getTv_owner() {
        return tv_owner;
    }

    public void setTv_owner(String tv_owner) {
        this.tv_owner = tv_owner;
    }

    public String getTv_type() {
        return tv_type;
    }

    public void setTv_type(String tv_type) {
        this.tv_type = tv_type;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
